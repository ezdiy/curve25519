#include "test/common.h"

TEST_IMPL(field_add) {
  curve25519_num_t a;
  curve25519_num_t b;
  static uint8_t a_bin[32] = {
    0xec, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
    0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
    0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
    0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0x7f
  };

  curve25519_num_from_bin(&a, a_bin);
  curve25519_num_from_bin(&b, a_bin);

  curve25519_num_add(&a, &b);
  curve25519_num_sub(&a, &b);
}
