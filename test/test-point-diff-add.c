TEST_IMPL(point_diff_add) {
  unsigned int i;

  for (i = 0; i < ARRAY_SIZE(diff_add_vectors); i++) {
    test_point_diff_add_vector_t* vec;
    curve25519_point_t p1;
    curve25519_point_t p2;
    curve25519_point_t diff;
    uint8_t bin_x[32];
    uint8_t bin_z[32];

    vec = &diff_add_vectors[i];

    curve25519_point_init_ex(&p1, vec->p1.x, vec->p1.z);
    curve25519_point_init_ex(&p2, vec->p2.x, vec->p2.z);
    curve25519_point_init_ex(&diff, vec->diff.x, vec->diff.z);

    curve25519_point_diff_add(&p1, &p1, &p2, &diff);
    curve25519_num_to_bin(bin_x, &p1.x);
    curve25519_num_to_bin(bin_z, &p1.z);

    CHECK_EQ(p1.normalized, 0, "additiona result should not be normalized");
    check_equal_data(bin_x, vec->expected.x, sizeof(bin_x),
                     vec->description);
    check_equal_data(bin_z, vec->expected.z, sizeof(bin_z),
                     vec->description);
  }
}
