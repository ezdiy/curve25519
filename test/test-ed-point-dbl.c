TEST_IMPL(ed_point_dbl) {
  unsigned int i;

  for (i = 0; i < ARRAY_SIZE(dbl_vectors); i++) {
    const test_ed_point_dbl_vector_t* vec;
    const test_ed_point_t* point;
    const test_ed_point_t* expected;
    uint8_t bin[32];
    curve25519_ed_point_t p;

    vec = &dbl_vectors[i];
    point = &vec->p;
    expected = &vec->expected;

    curve25519_ed_point_init_ex(&p, point->x, point->y, point->z, point->t);

    curve25519_ed_point_dbl(&p, &p);

    curve25519_num_to_bin(bin, &p.x);
    check_equal_data(bin, expected->x, sizeof(bin), vec->description);
    curve25519_num_to_bin(bin, &p.y);
    check_equal_data(bin, expected->y, sizeof(bin), vec->description);
    curve25519_num_to_bin(bin, &p.z);
    check_equal_data(bin, expected->z, sizeof(bin), vec->description);
    curve25519_num_to_bin(bin, &p.t);
    check_equal_data(bin, expected->t, sizeof(bin), vec->description);
  }
}
