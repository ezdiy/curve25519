TEST_IMPL(point_dbl) {
  unsigned int i;

  /*
  curve25519_point_t p;
  curve25519_point_t out;
  test_point_dbl_vector_t* vec;

  vec = &dbl_vectors[0];
  curve25519_point_init_ex(&p, vec->p.x, vec->p.z);
  for (i = 0; i < 100000; i++) {
    for (int j = 0; j < 255; j++) {
      curve25519_point_diff_add(&out, &p, &p, &p);
      curve25519_point_dbl(&out, &p);
    }
    curve25519_point_normalize(&out);
  }
  return;
  */

  for (i = 0; i < ARRAY_SIZE(dbl_vectors); i++) {
    test_point_dbl_vector_t* vec;
    curve25519_point_t p;
    uint8_t bin_x[32];
    uint8_t bin_z[32];

    vec = &dbl_vectors[i];

    curve25519_point_init_ex(&p, vec->p.x, vec->p.z);
    curve25519_point_dbl(&p, &p);
    curve25519_num_to_bin(bin_x, &p.x);
    curve25519_num_to_bin(bin_z, &p.z);

    CHECK_EQ(p.normalized, 0, "doubled point should not be normalized");
    check_equal_data(bin_x, vec->expected.x, sizeof(bin_x),
                     vec->description);
    check_equal_data(bin_z, vec->expected.z, sizeof(bin_z),
                     vec->description);
  }
}
