#include "test/common.h"

TEST_IMPL(field_combined) {
  curve25519_num_t out;
  curve25519_num_t a;
  curve25519_num_t b;
  curve25519_num_t c;
  curve25519_num_t d;
  uint8_t a_bin[32] = {
    0x36, 0x0a, 0x97, 0x3a, 0x96, 0xac, 0x1b, 0xff,
    0x4d, 0x5c, 0xb6, 0x8d, 0x3f, 0xcb, 0xd9, 0xcd,
    0x2f, 0x83, 0x83, 0x5d, 0x1d, 0x38, 0x48, 0x3a,
    0xa1, 0x31, 0x40, 0x59, 0x0a, 0x09, 0x08, 0x6e
  };
  uint8_t b_bin[32] = {
    0x0e, 0x78, 0xff, 0xa3, 0x38, 0x85, 0x7d, 0xca,
    0x88, 0xaf, 0xe7, 0xce, 0xa0, 0x1c, 0x8e, 0x30,
    0x19, 0xb2, 0xdc, 0x0d, 0x12, 0x2c, 0xd3, 0xe1,
    0x3f, 0xfa, 0x54, 0xe7, 0x4c, 0x38, 0x20, 0x3b
  };
  uint8_t c_bin[32] = {
    0x24, 0x17, 0xc5, 0xca, 0x7a, 0x35, 0x9f, 0xc7,
    0x07, 0x13, 0x0e, 0x3a, 0x5f, 0x09, 0x2c, 0x9b,
    0x54, 0x57, 0xe6, 0xeb, 0x2f, 0x79, 0x3c, 0xe2,
    0x18, 0x18, 0xc8, 0xe7, 0xac, 0x86, 0x27, 0x27
  };
  uint8_t d_bin[32] = {
    0x79, 0x2e, 0x75, 0x87, 0x8f, 0x11, 0xb2, 0xb4,
    0xeb, 0x7f, 0xbc, 0xb3, 0x29, 0x3a, 0xc9, 0xcb,
    0x2d, 0xf4, 0x6b, 0x09, 0x7a, 0x72, 0x02, 0xcb,
    0xf4, 0xb1, 0x7a, 0xbb, 0xf9, 0x7e, 0x08, 0x65
  };
  uint8_t expected[32] = {
    0x9b, 0xa1, 0xc3, 0x55, 0xf5, 0xb9, 0x6d, 0xce,
    0xd3, 0x71, 0x6e, 0xce, 0x8f, 0xa7, 0xe4, 0x87,
    0x61, 0xe3, 0xb9, 0xde, 0x8b, 0x78, 0x41, 0xb9,
    0xda, 0xf9, 0x4c, 0xb7, 0xc4, 0x74, 0x93, 0x56
  };
  uint8_t bin[32];

  curve25519_num_from_bin(&a, a_bin);
  curve25519_num_from_bin(&b, b_bin);
  curve25519_num_from_bin(&c, c_bin);
  curve25519_num_from_bin(&d, d_bin);

  curve25519_num_copy(&out, &a);

  curve25519_num_add(&out, &out, &b);
  curve25519_num_sub(&out, &out, &c);
  curve25519_num_mul(&out, &d);
  curve25519_num_add(&out, &out, &c);
  curve25519_num_sub(&out, &out, &d);
  curve25519_num_mul(&out, &b);

  curve25519_num_to_bin(bin, &out);

  check_equal_data(bin, expected, sizeof(bin), "combined");
}
