TEST_IMPL(ed_point_scalar_mul) {
  unsigned int i;

  for (i = 0; i < ARRAY_SIZE(mul_vectors); i++) {
    const test_ed_point_mul_vector_t* vec;
    const test_ed_point_t* point;
    uint8_t bin[32];
    curve25519_ed_point_t p;

    vec = &mul_vectors[i];
    point = &vec->p;

    curve25519_ed_point_init_ex(&p, point->x, point->y, point->z, point->t);
    curve25519_ed_point_scalar_mul(&p, &p, vec->scalar);
    curve25519_ed_point_normalize(&p);

    curve25519_num_to_bin(bin, &p.x);
    check_equal_data(bin, vec->expected.x, sizeof(bin), vec->description);
    curve25519_num_to_bin(bin, &p.y);
    check_equal_data(bin, vec->expected.y, sizeof(bin), vec->description);
  }
}
